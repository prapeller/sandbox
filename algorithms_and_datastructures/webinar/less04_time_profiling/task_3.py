import cProfile
import timeit

number = 1234567890
ncalls = 1000000


def revers_1(enter_num, revers_num=0):
    if enter_num == 0:
        return revers_num
    else:
        num = enter_num % 10
        revers_num = (revers_num + num / 10) * 10
        enter_num //= 10
        return revers_1(enter_num, revers_num)


def revers_2(enter_num, revers_num=0):
    while enter_num != 0:
        num = enter_num % 10
        revers_num = (revers_num + num / 10) * 10
        enter_num //= 10
    return revers_num


def revers_3(enter_num):
    enter_num = str(enter_num)
    revers_num = enter_num[::-1]
    return revers_num


def revers_4(enter_num):
    return ''.join(list(str(enter_num)).__reversed__())


cProfile.run('print(revers_1(number))')
print(f"{timeit.timeit('revers_1(number)', globals=globals(), number=ncalls)} sec")

cProfile.run('print(revers_2(number))')
print(f"{timeit.timeit('revers_2(number)', globals=globals(), number=ncalls)} sec")

cProfile.run('print(revers_3(number))')
print(f"{timeit.timeit('revers_3(number)', globals=globals(), number=ncalls)} sec")

cProfile.run('print(revers_4(number))')
print(f"{timeit.timeit('revers_4(number)', globals=globals(), number=ncalls)} sec")

